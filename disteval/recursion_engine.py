"""
disteval.recursion_engine — Recursive self-improvement engine for agentic tasks.

OVERVIEW
────────
This module decomposes STUCK and high-gap RECOVERABLE tasks into sub-task
RMDPs (Recursive Markov Decision Processes) with explicit entry/exit points.
It builds on the right-tail taxonomy from `disteval.right_tail` and the
trajectory monitor/memory primitives to identify structural boundaries and
produce a sub-task graph that can be turned into training environments.

The engine is default-disabled and opt-in. When not enabled, the existing
flat `SelfEngine` behavior is unchanged.

KEY DESIGN CHOICES
──────────────────
- Boundaries are derived from test-script checkpoints when available (via
  `disteval.test_suite_parser`). This gives semantically meaningful sub-tasks.
- When checkpoint alignment is unavailable, structural phase boundaries from
  `TrajectoryMonitor` are used as a fallback.
- Sub-task scores are estimated from the parent task's total score using the
  checkpoint weights. This is a best-effort approximation until the test harness
  emits per-checkpoint rewards.
- Recursion depth is capped at `max_depth` (default 3) and a monotone
  complexity check prevents infinite decomposition of STUCK tasks.
"""
from __future__ import annotations

import json
import os
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional


from .right_tail import (
    TaskOutcomeProfile,
    RightTailReport,
    task_outcome_profile,
)
from .test_suite_parser import CheckpointSpec, parse_all_tasks
from .trajectory_monitor import TrajectoryMonitor
from .trajectory_memory import TrajectoryMemory


@dataclass
class PhaseBoundary:
    """One RMDP entry/exit pair within a parent trajectory."""

    entry_step: int
    exit_step: Optional[int] = None
    label: str = ""
    confidence: float = 0.0
    tool_signature: tuple[str, ...] = field(default_factory=tuple)
    phase_tag: str = ""
    checkpoint_id: str = ""
    reward_delta: float = 0.0
    weight: float = 1.0
    source: str = "structural_divergence"


@dataclass
class SubTaskDefinition:
    """A callable sub-task RMDP component generated by RecursionEngine."""

    sub_task_id: str
    parent_task: str
    sub_task_depth: int
    entry_step: int
    exit_step: int
    phase_tag: str
    instruction: str
    estimated_q_star: float
    estimated_q_bar: float
    kind: str  # "solid" | "recoverable" | "stuck"
    reward_delta: float = 0.0
    weight: float = 1.0
    checkpoint_id: str = ""
    source: str = "structural_divergence"  # "checkpoint" | "structural_divergence" | "memory"
    entry_traj_path: Optional[str] = None
    exit_traj_path: Optional[str] = None


@dataclass
class RMDPNode:
    """One node in the recursive task decomposition tree."""

    task: str
    profile: TaskOutcomeProfile
    depth: int = 0
    parent: Optional[str] = None
    sub_task: Optional[SubTaskDefinition] = None
    children: list["RMDPNode"] = field(default_factory=list)
    phase_boundaries: list[PhaseBoundary] = field(default_factory=list)
    training_pairs: list[dict] = field(default_factory=list)
    decomposed_reason: str = "recoverable"  # "recoverable" | "stuck" | "deep_gap"
    recursively_stuck: bool = False

    @property
    def is_leaf(self) -> bool:
        return not self.children

    @property
    def sub_task_ids(self) -> list[str]:
        return [
            c.sub_task.sub_task_id for c in self.children
            if c.sub_task is not None
        ]


@dataclass
class SubTaskGraph:
    """JSON-serializable graph of parent/sub-task relationships."""

    parent_tasks: list[str] = field(default_factory=list)
    sub_tasks: list[SubTaskDefinition] = field(default_factory=list)
    edges: list[tuple[str, str]] = field(default_factory=list)
    profiles: dict[str, TaskOutcomeProfile] = field(default_factory=dict)

    def to_dict(self) -> dict:
        """Serialize to a dict for JSON export."""
        return {
            "parent_tasks": self.parent_tasks,
            "sub_tasks": [
                {
                    "sub_task_id": s.sub_task_id,
                    "parent_task": s.parent_task,
                    "sub_task_depth": s.sub_task_depth,
                    "entry_step": s.entry_step,
                    "exit_step": s.exit_step,
                    "phase_tag": s.phase_tag,
                    "instruction": s.instruction,
                    "estimated_q_star": s.estimated_q_star,
                    "estimated_q_bar": s.estimated_q_bar,
                    "kind": s.kind,
                    "reward_delta": s.reward_delta,
                    "weight": s.weight,
                    "checkpoint_id": s.checkpoint_id,
                    "source": s.source,
                    "entry_traj_path": s.entry_traj_path,
                    "exit_traj_path": s.exit_traj_path,
                }
                for s in self.sub_tasks
            ],
            "edges": self.edges,
        }

    def save(self, path: str) -> None:
        """Save graph to JSON."""
        os.makedirs(os.path.dirname(os.path.abspath(path)) or ".", exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            json.dump(self.to_dict(), f, indent=2)


@dataclass
class RecursionEngineConfig:
    """Tunable parameters for RecursionEngine."""

    max_depth: int = 3
    divergence_confidence: float = 0.70
    max_phase_boundaries: int = 5
    min_monotone_difficulty: float = 0.0
    reward_propagation: str = "weighted_sum"  # "weighted_sum" | "max" | "right_tail"
    enable_decompose_stuck: bool = True
    enable_decompose_recoverable: bool = True
    min_sub_task_score: float = 0.0
    require_checkpoint_alignment: bool = False
    max_stack_nodes: int = 100
    memory_retrieval_k: int = 3


class RecursionEngine:
    """
    Decompose agentic tasks into sub-task RMDPs.

    The engine is instantiated with a TrajectoryMonitor (and optionally a
    TrajectoryMemory) and called on a RightTailReport to produce a SubTaskGraph.
    """

    def __init__(
        self,
        monitor: TrajectoryMonitor,
        memory: Optional[TrajectoryMemory] = None,
        config: Optional[RecursionEngineConfig] = None,
        agent_name: str = "agent",
        model_name: str = "unknown",
        tasks_dir: str = "tasks",
    ) -> None:
        self.monitor = monitor
        self.memory = memory
        self.config = config or RecursionEngineConfig()
        self.agent_name = agent_name
        self.model_name = model_name
        self.tasks_dir = tasks_dir

        # Load checkpoint specs for all tasks eagerly.
        self._checkpoint_specs: dict[str, list[CheckpointSpec]] = parse_all_tasks(
            tasks_dir
        )
        self._node_count = 0

    def decompose(
        self,
        report: RightTailReport,
        traj_records: Optional[list] = None,
    ) -> SubTaskGraph:
        """
        Build a sub-task graph from STUCK and high-gap RECOVERABLE tasks in report.

        Parameters
        ----------
        report : RightTailReport
            The right-tail analysis for one agent.
        traj_records : list[TrajectoryRecord] | None
            Optional trajectory records. If None, uses monitor.records.

        Returns
        -------
        SubTaskGraph
            A graph of parent tasks and their sub-task decompositions.
        """
        if traj_records is None:
            traj_records = getattr(self.monitor, "records", [])

        graph = SubTaskGraph()
        roots: list[RMDPNode] = []

        # Build a map of task -> trajectory records for quick lookup.
        self._traj_records_by_task: dict[str, list] = {}
        for rec in traj_records:
            task = getattr(rec, "task_path", "") or getattr(rec, "task", "")
            # Normalize to task directory name if possible.
            task_name = Path(task).name if task else ""
            self._traj_records_by_task.setdefault(task_name, []).append(rec)

        for profile in report.profiles:
            # Only decompose non-SOLID tasks.
            if profile.kind == "solid":
                continue
            if profile.kind == "stuck" and not self.config.enable_decompose_stuck:
                continue
            if profile.kind == "recoverable" and not self.config.enable_decompose_recoverable:
                continue

            node = self._decompose_task(
                profile=profile,
                task_records=self._traj_records_by_task.get(
                    Path(str(profile.task)).name, []
                ),
                depth=0,
            )
            if node.children:
                roots.append(node)
                graph.parent_tasks.append(profile.task)
                for child in node.children:
                    if child.sub_task is not None:
                        graph.sub_tasks.append(child.sub_task)
                        graph.edges.append((profile.task, child.sub_task.sub_task_id))
                        graph.profiles[child.sub_task.sub_task_id] = child.profile

        return graph

    def _decompose_task(
        self,
        profile: TaskOutcomeProfile,
        task_records: list,
        depth: int,
        parent: Optional[str] = None,
    ) -> RMDPNode:
        """Decompose a single task into a tree of RMDP sub-tasks."""
        self._node_count += 1
        node = RMDPNode(
            task=profile.task,
            profile=profile,
            depth=depth,
            parent=parent,
            decomposed_reason=profile.kind,
        )

        # Termination: depth cap, complexity check, or max nodes.
        if depth >= self.config.max_depth:
            return node
        if self._node_count >= self.config.max_stack_nodes:
            return node

        # Find boundaries for this task.
        boundaries = self._find_boundaries(profile, task_records)
        if len(boundaries) < 2:
            # No useful decomposition.
            return node

        # Estimate sub-task scores.
        sub_task_scores = self._estimate_sub_task_scores(
            profile.scores, boundaries, profile.task
        )

        for idx, boundary in enumerate(boundaries[:-1]):
            next_boundary = boundaries[idx + 1]
            entry_step = boundary.entry_step if boundary.entry_step is not None else 0
            exit_step = next_boundary.exit_step
            if exit_step is None:
                exit_step = -1

            sub_task_id = f"{profile.task}::phase-{idx}"
            checkpoint_id = boundary.checkpoint_id or sub_task_id

            scores = sub_task_scores[idx]
            sub_profile = task_outcome_profile(
                task=sub_task_id,
                scores=scores,
                model=self.model_name,
                difficulty=profile.difficulty,
                parent_task=profile.task,
                sub_task_depth=depth + 1,
            )

            sub_task = SubTaskDefinition(
                sub_task_id=sub_task_id,
                parent_task=profile.task,
                sub_task_depth=depth + 1,
                entry_step=entry_step,
                exit_step=exit_step,
                phase_tag=boundary.phase_tag or "unknown",
                instruction=self._derive_instruction(profile.task, boundary),
                estimated_q_star=sub_profile.q_star,
                estimated_q_bar=sub_profile.q_bar,
                kind=sub_profile.kind,
                reward_delta=boundary.reward_delta,
                weight=boundary.weight,
                checkpoint_id=checkpoint_id,
                source=boundary.source,
            )

            child_node = RMDPNode(
                task=sub_task_id,
                profile=sub_profile,
                depth=depth + 1,
                parent=profile.task,
                sub_task=sub_task,
                decomposed_reason=sub_profile.kind,
            )

            # Recurse if the sub-task is STUCK and we have budget.
            if sub_profile.kind == "stuck" and depth + 1 < self.config.max_depth:
                child_records = self._slice_records(task_records, entry_step, exit_step)
                child_node = self._decompose_task(
                    profile=sub_profile,
                    task_records=child_records,
                    depth=depth + 1,
                    parent=profile.task,
                )
                child_node.sub_task = sub_task
                child_node.decomposed_reason = "stuck"

            node.children.append(child_node)

        # Detect recursively stuck: all children are STUCK with no further children.
        if all(
            c.profile.kind == "stuck" and not c.children
            for c in node.children
        ):
            for c in node.children:
                c.recursively_stuck = True

        return node

    def _find_boundaries(
        self,
        profile: TaskOutcomeProfile,
        task_records: list,
    ) -> list[PhaseBoundary]:
        """
        Find phase boundaries for a task, preferring checkpoint alignment.

        Structural boundaries from the trajectory monitor are the primary source.
        When checkpoint specs are available, they are merged in to label each
        boundary with the correct checkpoint_id, reward_delta, and description.
        The number of sub-tasks is aligned to the number of checkpoints.

        Returns a list of PhaseBoundary objects. The first boundary always has
        entry_step=0 and the last boundary always has exit_step=None (terminal).
        """
        task_name = Path(str(profile.task)).name
        specs = self._checkpoint_specs.get(task_name, [])

        if self.config.require_checkpoint_alignment and not specs:
            return []

        # Always get structural boundaries first; they provide concrete step indices.
        boundaries = self._structural_boundaries(profile, task_records)

        if specs:
            # Align the number of boundaries to the number of checkpoints.
            n_specs = len(specs)
            # Remove terminal boundary before alignment; re-add it afterwards.
            if boundaries and boundaries[-1].phase_tag == "terminal":
                terminal = boundaries.pop()
            else:
                terminal = None

            if len(boundaries) > n_specs:
                # Drop extra structural boundaries to match checkpoint count.
                boundaries = boundaries[:n_specs]
            elif len(boundaries) < n_specs:
                # Pad with synthetic boundaries based on the last known step index.
                last_step = boundaries[-1].exit_step if boundaries else None
                last_step = last_step if last_step is not None else 10
                while len(boundaries) < n_specs:
                    boundaries.append(
                        PhaseBoundary(
                            entry_step=None,
                            exit_step=last_step + len(boundaries),
                            label="synthetic",
                            confidence=0.5,
                            phase_tag="unknown",
                        )
                    )

            # Re-add the terminal boundary.
            if terminal is not None:
                boundaries.append(terminal)
            else:
                boundaries.append(
                    PhaseBoundary(
                        entry_step=None,
                        exit_step=None,
                        label="terminal",
                        confidence=1.0,
                        phase_tag="terminal",
                    )
                )

            # Merge checkpoint specs into structural boundaries by index.
            for idx, spec in enumerate(specs):
                if idx < len(boundaries) - 1:
                    boundaries[idx].checkpoint_id = spec.checkpoint_id
                    boundaries[idx].reward_delta = spec.reward_weight
                    boundaries[idx].weight = spec.reward_weight
                    boundaries[idx].source = "checkpoint"
                    # Use the checkpoint description as a label if available.
                    if spec.description:
                        boundaries[idx].label = spec.description

        return boundaries

    def _checkpoint_boundaries(
        self,
        specs: list[CheckpointSpec],
    ) -> list[PhaseBoundary]:
        """Build phase boundaries from parsed checkpoint specs."""
        boundaries = []
        cumulative = 0.0
        for idx, spec in enumerate(specs):
            entry_step = 0 if idx == 0 else None
            boundaries.append(
                PhaseBoundary(
                    entry_step=entry_step,
                    exit_step=None,  # will be set by caller
                    label=f"phase-{idx}",
                    confidence=1.0,
                    phase_tag="verify",
                    checkpoint_id=spec.checkpoint_id,
                )
            )
            cumulative += spec.reward_weight

        # Terminal boundary.
        boundaries.append(
            PhaseBoundary(
                entry_step=None,
                exit_step=None,
                label="terminal",
                confidence=1.0,
                phase_tag="terminal",
            )
        )
        return boundaries

    def _structural_boundaries(
        self,
        profile: TaskOutcomeProfile,
        task_records: list,
    ) -> list[PhaseBoundary]:
        """Build phase boundaries from TrajectoryMonitor structural signals."""
        if not task_records:
            return []

        # Use the highest-scoring trajectory as the reference.
        high_record = max(task_records, key=lambda r: r.score)
        boundaries_data = self.monitor.find_phase_boundaries(
            high_record.traj_path,
            min_confidence=self.config.divergence_confidence,
            max_boundaries=self.config.max_phase_boundaries,
        )

        boundaries = []
        for idx, bd in enumerate(boundaries_data):
            boundaries.append(
                PhaseBoundary(
                    entry_step=0 if idx == 0 else None,
                    exit_step=bd["step_index"],
                    label=f"phase-{idx}",
                    confidence=bd["confidence"],
                    tool_signature=tuple([bd["tool_name"]]),
                    phase_tag=bd["phase_tag"],
                )
            )

        boundaries.append(
            PhaseBoundary(
                entry_step=None,
                exit_step=None,
                label="terminal",
                confidence=1.0,
                phase_tag="terminal",
            )
        )
        return boundaries

    def _estimate_sub_task_scores(
        self,
        scores: list[float],
        boundaries: list[PhaseBoundary],
        task_name: str,
    ) -> list[list[float]]:
        """
        Estimate per-sub-task scores from parent total scores.

        Uses checkpoint weights if available, otherwise uniform weights.
        """
        task_base = Path(str(task_name)).name
        specs = self._checkpoint_specs.get(task_base, [])
        if specs:
            weights = [s.reward_weight for s in specs]
        else:
            n = len(boundaries) - 1
            weights = [1.0 / n] * n

        # Cumulative thresholds to infer which checkpoints passed.
        cumulative = [0.0]
        for w in weights:
            cumulative.append(cumulative[-1] + w)

        result: list[list[float]] = [[] for _ in range(len(boundaries) - 1)]
        for total in scores:
            # Each checkpoint i is considered passed if total >= cumulative[i+1]
            # (with small tolerance). Its score is weights[i] if passed, else 0.0.
            for i in range(len(boundaries) - 1):
                threshold = cumulative[i + 1] - 1e-6
                if total >= threshold:
                    result[i].append(weights[i])
                else:
                    result[i].append(0.0)
        return result

    def _slice_records(
        self,
        task_records: list,
        entry_step: int,
        exit_step: int,
    ) -> list:
        """Return synthetic records for a sub-task window."""
        import copy
        sliced = []
        for rec in task_records:
            seq = list(getattr(rec, "tool_sequence", []))
            n = len(seq)
            start = max(0, entry_step)
            end = n if exit_step < 0 else min(n, exit_step)
            sub_seq = seq[start:end]
            if sub_seq:
                new_rec = copy.copy(rec)
                new_rec.tool_sequence = sub_seq
                new_rec.n_steps = len(sub_seq)
                new_rec.entry_step = entry_step
                new_rec.exit_step = exit_step
                sliced.append(new_rec)
        return sliced

    def _derive_instruction(self, task: str, boundary: PhaseBoundary) -> str:
        """Build a human-readable sub-task instruction."""
        base = Path(str(task)).name
        spec = None
        specs = self._checkpoint_specs.get(base, [])
        for sp in specs:
            if sp.checkpoint_id == boundary.checkpoint_id:
                spec = sp
                break

        if spec and spec.description:
            return f"Complete phase '{spec.description}' for task {task}"
        if boundary.phase_tag:
            return f"Complete the {boundary.phase_tag} phase of task {task}"
        return f"Complete sub-task {boundary.checkpoint_id or 'unknown'} for task {task}"

    def compute_recursive_gap(self, root: RMDPNode) -> float:
        """
        Compute the total recursive gap for a decomposition tree.

        For a RECOVERABLE parent, the recursive gap is the weighted sum of the
        gaps of its non-SOLID sub-tasks.
        """
        if not root.children:
            return root.profile.gap

        gap = 0.0
        for child in root.children:
            if child.profile.kind != "solid":
                child_gap = self.compute_recursive_gap(child)
                weight = child.sub_task.weight if child.sub_task else 1.0
                gap += weight * child_gap
        return gap

    def to_sub_task_graph(self, roots: list[RMDPNode]) -> SubTaskGraph:
        """Convert a list of RMDPNode roots into a SubTaskGraph."""
        graph = SubTaskGraph()
        for root in roots:
            graph.parent_tasks.append(root.task)
            for child in root.children:
                if child.sub_task is not None:
                    graph.sub_tasks.append(child.sub_task)
                    graph.edges.append((root.task, child.sub_task.sub_task_id))
                    graph.profiles[child.sub_task.sub_task_id] = child.profile
        return graph
